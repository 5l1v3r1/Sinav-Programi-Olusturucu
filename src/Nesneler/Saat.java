package Nesneler;

public class Saat {

    public int h, m;

    /**
     * Sınav saatini belirtmek için kullanılır. Saat ve Dakika değerlerini parametre alır.
     * @param h Saat. (Örn: 16)
     * @param m Dakika. (Örn: 45)
     */
    public Saat(int h, int m) {
        this.h = h;
        this.m = m;
    }
}
